            <ul class="list-group">
                <li class="list-group-item list-group-item-secondary"><b>CRL (Commercial Readiness Level) o Nivel de madurez comercial</b></li>
                <li class="list-group-item"><b>CRL1:</b> El conocimiento de las aplicaciones, los casos de uso y las limitaciones del mercado es limitado e incidental, o aún no se ha obtenido</li>
                <li class="list-group-item"><b>CRL2:</b> Existe una familiaridad superficial con aplicaciones potenciales, mercados y tecnologías / productos competitivos existentes. La investigación de mercado se deriva principalmente de fuentes secundarias. Las ideas de productos basadas en la nueva tecnología pueden existir, pero son especulativas y no validadas.</li>
                <li class="list-group-item"><b>CRL3:</b> Una comprensión más desarrollada de aplicaciones potenciales, casos de uso de tecnología, requisitos / restricciones del mercado y una familiaridad con tecnologías y productos competitivos permite la consideración inicial de la tecnología como producto. Se crean una o más hipótesis de producto "hombre de paja", y se pueden refinar iterativamente en base a los datos de más tecnología y análisis de mercado. El análisis de comercialización incorpora una mayor dependencia de la investigación primaria y considera no solo las realidades actuales del mercado sino también los requisitos futuros esperados.</li>
