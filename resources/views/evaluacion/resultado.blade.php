                <li class="list-group-item list-group-item-secondary"><b>TRL (Technology Readiness Levels) o Nivel de Madurez Tecnológica</b></li>
                <li class="list-group-item"><b>TRL 1:</b> Principios básicos reportados de la nueva tecnología propuesta. Este nivel se caracteriza por el desarrollo de investigación básica, la revisión y publicación de artículos científicos, el desarrollo y uso básico del conocimiento necesario para iniciar una tecnología, sin un propósito definido de aplicación. Se desarrolla en un entorno de laboratorio.</li>
                <li class="list-group-item"><b>TRL 2:</b> Formulación del concepto o aplicación de la propuesta tecnológica. Este nivel se caracteriza por el desarrollo de investigación aplicada. Explora las aplicaciones prácticas de la propuesta tecnológica, siendo las posibles aplicaciones aún especulativas. Se sustentan en el análisis del estado del arte acotado al concepto de aplicación tecnológico. En este nivel los aspectos de propiedad intelectual adquieren importancia y continúan en adelante. Se desarrolla en un entorno de laboratorio.</li>
                <li class="list-group-item"><b>TRL 3:</b> Desarrollo experimental o prueba del concepto tecnológico (Prueba de concepto). Este nivel se caracteriza por realizar la ¨Prueba de concepto¨ tecnológico, donde se demuestra que los principios básicos previstos, sustentan el desarrollo de la tecnología que serán ciertamente aplicables. Se comienza a demostrar la viabilidad de la nueva tecnología a través de estudios analíticos y de laboratorio. Se desarrolla en un entorno de laboratorio.</li>
                <li class="list-group-item"><b>TRL 4:</b> Validación tecnológica en entorno de laboratorio - Pruebas de baja fidelidad. Este nivel se caracteriza por el desarrollo de un prototipo tecnológico con componentes básicos integrados para ser probados en un ambiente simulado, con resultados donde se ponen a prueba sus principales características. Se desarrolla en un entorno de laboratorio.</li>
                <li class="list-group-item"><b>TRL 5:</b> Validación tecnológica en entorno de laboratorio - Pruebas de alta fidelidad. Este nivel se caracteriza por desarrollar la tecnología con los componentes integrados para ser validados. Considera prototipos a escala en un entorno simulado cercano al real con pruebas de sus características completas. En este nivel el paquete tecnológico adquiere importancia y continúa en adelante.</li>
                <li class="list-group-item"><b>TRL 6:</b> Demostración de funcionamiento del prototipo un entorno cercano al real. Este nivel se caracteriza por encontrarse en el estado de validación del sistema o prototipo desarrollado en una ambiente simulado de alta fidelidad o un entorno operacional controlado o una planta piloto.</li>
                <li class="list-group-item"><b>TRL 7:</b> Demostración de funcionamiento del prototipo un entorno operacional real. Este nivel se caracteriza por encontrarse en el estado de validación del sistema o prototipo desarrollado en un entorno real (operacional). El prototipo/sistema/producto mínimo viable ya es considerado una innovación de aquí en adelante e incluye una demostración técnica y cualitativa. Se desarrolla en un entorno real.</li>
                <li class="list-group-item"><b>TRL 8:</b> Tecnología finalizada validada y certificada. Este nivel se caracteriza por tener una tecnología validada o certificada luego de pruebas en entorno reales y cumplimiento de normativas nacionales. La tecnología ha logrado convertirse en un producto/servicio comercializable, su riesgo es bajo y puede ser transferido de forma completa. Se desarrolla en un entorno real.</li>
                <li class="list-group-item"><b>TRL 9:</b> Tecnología en proceso de implementación o comercialización (emprendimiento). En definición: Este nivel se caracteriza por encontrarse en el proceso de implementación o comercialización del producto/servicio para satisfacer necesidades de mercado. La nueva tecnología se encuentra totalmente disponible y se puede utilizar en cualquier entorno real. Se desarrolla en un entorno real.</li>
